// Create a new multi-agent run
export const create = mutation({
  args: {
    datasetName: v.string(),
    uploadId: v.optional(v.id("uploads")),
  },
  handler: async (ctx, args) => {
    const runId = await ctx.db.insert("runs", {
      status: "running",
      step: "Observe",
      activeAgent: "Intake",
      logs: [
        {
          timestamp: Date.now(),
          agent: "Intake",
          message: `Ingesting dataset: ${args.datasetName}`,
          type: "info",
        },
      ],
      uploadId: args.uploadId,
      datasetName: args.datasetName,
      createdAt: Date.now(),
      updatedAt: Date.now(),
    });
    return runId;
  },
});

// Retrieve details for a specific run
export const get = query({
  args: { runId: v.id("runs") },
  handler: async (ctx, args) => {
    return await ctx.db.get(args.runId);
  },
});

// Retrieve the latest run (for tab-fallback navigation)
export const getLatest = query({
  args: {},
  handler: async (ctx) => {
    return await ctx.db
      .query("runs")
      .order("desc")
      .first();
  },
});

// Update the run state machine status/step
export const updateStep = mutation({
  args: {
    runId: v.id("runs"),
    status: v.string(),
    step: v.string(),
    activeAgent: v.string(),
    logMessage: v.string(),
    logType: v.string(), // "info" | "success" | "warn" | "error"
  },
  handler: async (ctx, args) => {
    const run = await ctx.db.get(args.runId);
    if (!run) throw new Error("Run not found");

    const updatedLogs = [
      ...run.logs,
      {
        timestamp: Date.now(),
        agent: args.activeAgent,
        message: args.logMessage,
        type: args.logType,
      },
    ];

    await ctx.db.patch(args.runId, {
      status: args.status,
      step: args.step,
      activeAgent: args.activeAgent,
      logs: updatedLogs,
      updatedAt: Date.now(),
    });
  },
});

